//function.js
//Normal functions and arrow functions


// Normal function
function add(a, b) {
  return a + b;
}

// Arrow function
const multiply = (a, b) => {
  return a * b;
};

// Short arrow function
const subtract = (a, b) => a - b;

console.log(add(2, 3));
console.log(multiply(4, 5));
console.log(subtract(10, 3));